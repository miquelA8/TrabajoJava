public class Repaso

{
    public static void main(final String[] args) 
    {
        final int val = 6;
        final int val2 = 2;
        final int summ = val + val2;
        System.out.println("Suma entre " + val + " y " + val2 +": " + summ );
        final int ress = val - val2;
        System.out.println("Resta entre " + val + " y " + val2 +": " + ress);
        final int multt = val * val2;
        System.out.println("Multiplicación entre " + val + " y " + val2 +": " + multt);
        final int divv = val / val2;
        System.out.println("División entre " + val + " y " + val2 +": " + divv);
        
    }

}